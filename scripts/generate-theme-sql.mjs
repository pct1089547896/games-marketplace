// Generate SQL INSERT statements
function generateThemeSQL() {
  let sql = '-- Auto-generated theme presets\n';
  sql += '-- This section inserts all 31 preset themes into the database\n\n';

  allThemes.forEach((theme, index) => {
    const cssVarsJSON = JSON.stringify(theme.cssVariables).replace(/'/g, "''");
    
    sql += `-- Theme ${index + 1}: ${theme.displayName}\n`;
    sql += `INSERT INTO themes (name, display_name, description, category, css_variables, is_preset)\n`;
    sql += `VALUES (\n`;
    sql += `  '${theme.name}',\n`;
    sql += `  '${theme.displayName}',\n`;
    sql += `  '${theme.description}',\n`;
    sql += `  '${theme.category}',\n`;
    sql += `  '${cssVarsJSON}'::jsonb,\n`;
    sql += `  true\n`;
    sql += `);\n\n`;
  });

  return sql;
}

